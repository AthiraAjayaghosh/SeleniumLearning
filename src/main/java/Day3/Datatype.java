package Day3;

public class Datatype {

    public static void main(String[] args) {

        byte byt = 127;
        System.out.println("byte value :" + byt);

        short shot = 129;
        System.out.println("short value :" + shot);

        int init = 21882;
        System.out.println("int value :" + init);

        long lng = 891271202917257L;
        System.out.println("long value :" + lng);


        float flt = 5.241561781f;
        System.out.println("float value :" + flt);

        double dble = 8.928912109920183;
        System.out.println("double value :" + dble);

        char ch = 'A';
        System.out.println("Character value :" + ch);

        boolean bln = true;
        System.out.println("boolean value :" + bln);

    }
}