package Day2;

public class BasicMath {

    public static void main(String[] args) {

        int sum = 10+20;
        System.out.println("Sum of numbers:" + sum);

        int mul = 10*20;
        System.out.println("Mul of numbers:" + mul);

        int sub = 20-10;
        System.out.println("Difference of numbers:" + sub);

    }
}
